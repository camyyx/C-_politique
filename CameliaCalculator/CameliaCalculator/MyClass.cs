using System;
namespace CameliaCalculator
{
    public class Calculator
    {
        public static int additions(int op1, int op2)
        {
            return (op1 + op2); 
        }
        public static long additions(long op1, long op2){
            return (op1 + op2);
        }
        public static float additions(float op1, float op2){
            return (op1 + op2);
        }

        public static int soustractions(int op1, int op2)
        {
            return (op1 - op2);
        }
        public static long soustractions(long op1, long op2)
        {
            return (op1 - op2);
        }
        public static float soustractions(float op1, float op2)
        {
            return (op1 - op2);
        }

        public static int multiplications(int op1, int op2)
        {
            return (op1 * op2);
        }
        public static long multiplications(long op1, long op2)
        {
            return (op1 * op2);
        }
        public static float multiplications(float op1, float op2)
        {
            return (op1 * op2);
        }

    }
}
